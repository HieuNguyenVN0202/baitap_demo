package UseVongLapDeDuyetMang;

public class InRaSoTu0Den10 {
    public static void main(String[] args) {
        System.out.println("Sử dụng vòng lặp for:");
        for (int i = 0; i <= 10; i++) {
            System.out.print(i + " ");
        }

        System.out.println("\nSử dụng vòng lặp while:");
        int j = 0;
        while (j <= 10) {
            System.out.print(j + " ");
            j++;
        }
    }
}
